/*
N단위로 끊어 범주화 시키고 싶을 때 주로 사용되는 floor 함수
=> floor(money/1000)*1000  # 1000 단위로 끊기
테이블 : PRODUCT
출력 컬럼 : FLOOR(PRICE/10000)*10000 AS PRICE_GROUP, PRODUCTS
조건) GROUP BY FLOOR(PRICE/10000)*10000
ORDER BY PRICE_GROUP
*/

SELECT CASE 
        WHEN PRICE < 10000 THEN 0
        ELSE FLOOR(PRICE/10000)*10000 
        END AS PRICE_GROUP, 
        COUNT(*) AS PRODUCTS
FROM PRODUCT
GROUP BY CASE
            WHEN PRICE < 10000 THEN 0
            ELSE FLOOR(PRICE/10000)*10000
         END
ORDER BY PRICE_GROUP;
